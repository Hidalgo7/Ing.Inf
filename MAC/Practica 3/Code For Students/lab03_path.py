    visitados = [False] * len(graph)
    porExaminar = [start]
    while(len(porExaminar) > 0 and not visitados[end]):
        nodo = porExaminar.pop();
        visitados[nodo] = True;
        for i in range(0,len(graph[nodo])):
            if graph[nodo][i] and not visitados[i]:
                porExaminar.append(i)
                
    return visitados[end]


     
     
